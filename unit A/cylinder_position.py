def compute_cartesian_coordinates(cylinders, cylinder_offset):
    result = []
    tmp = LegoLogfile()
    for c in cylinders:
        l = c[1] + cylinder_offset
        theta = tmp.beam_index_to_angle(c[0])
        x = l*cos(theta)
        y = l*sin(theta)
        result.append((x,y))
    return result


